
public class Character {
    
  // Fields (almost always private)
  private int score;
    
  public Character(int sc) {
    score = sc;
  }
  
  public Character() {
    score = 0;
  }
  
  public void setScore(int sc) {
    score = sc;
  }
  
  public int getScore() {
    return score;
  }

}
